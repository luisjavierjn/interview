package Questions.MathCharOperations;

public class Main {
    public static void main(String[] args) {
        System.out.println('a');
        System.out.println("a");
        System.out.println('a' + 0);
        System.out.println("a" + 0);
        System.out.println("b" + "a");
        System.out.println('b' + 'a');
        System.out.println("b" + 'a');
        System.out.println('b' + "a");
        System.out.println('b' - 'a');
    }
}
